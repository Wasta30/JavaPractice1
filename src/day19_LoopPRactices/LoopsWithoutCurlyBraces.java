package day19_LoopPRactices;

public class LoopsWithoutCurlyBraces {


    public static void main(String[] args) {

        // loops can be without curly braces but only when there is one statement inside the loop
        for (int i = 0; i < 5 ; i++)
            System.out.println("Batch 10");

// if two statement i need to give curly braces
        for (int i = 0; i < 5 ; i++){
            System.out.println("Batch 10");
            System.out.println("Wasta tariq");
        }


    }




}
