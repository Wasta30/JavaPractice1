package day18_NestedLoop;

public class NestedLoopsPractice2 {


    public static void main(String[] args) {


        for (int j = 0; j <10 ; j++) {// outer loop, wanted to repeat inner code fragment
            for (int i = 0; i < 10; i++) {// ww want to repeat this statemnt
                System.out.print("* "); // responsible for printing *

            }
            System.out.println();
        }








    }

}
