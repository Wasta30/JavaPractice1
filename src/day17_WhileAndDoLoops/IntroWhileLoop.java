package day17_WhileAndDoLoops;

public class IntroWhileLoop {

    public static void main(String[] args) {

        // second loop in java
        // everytime we have a while loop we will give boolean expression
        // if boolean exp is true it gets executed otherwise not
        // the difference between while loop and if statement is that only while loop gets executed multiple times but if statement execute one ti,me
        // repeated if statement
// if number of repeating is unknown use while loop

        if(true){
            System.out.println("Wooden Spoon");
        } // 1 time

        System.out.println("-------------------------------");

        while (true){ // execute infinte times now,it will terminated when the boolean expression u gave evaluated  to false
            System.out.println("Wooden Spoon");
        }



    }



}
