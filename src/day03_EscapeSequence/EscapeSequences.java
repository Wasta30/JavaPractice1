package day03_EscapeSequence;

/*
escape Sequences : must be given within double quote
\n: Starts a new line
\t : gives paragraph escape (tab button)
\\: when you give two back slash it gives one back slash
\": if u want to give double quote use it

 */

public class EscapeSequences {

    public static void main(String[] args) {


        System.out.println("Java \nPython \nC+");

        System.out.println("--------------");

        System.out.println("Hello Cydeo \nHow are you all today? \nIts nice to see you all!\n What class do we have next week ?");

        System.out.println("------------------");

        System.out.println("\tJava is a cool language");

        System.out.println("----------------");
        System.out.println("/\\"); // in order to print one back slash give two back slash


        System.out.println("My favorite tv show is \"Game of throne\""); // in order to give double quote use it \"




    }






}
